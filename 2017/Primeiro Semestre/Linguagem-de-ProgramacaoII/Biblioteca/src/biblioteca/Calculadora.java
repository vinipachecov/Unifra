package biblioteca;

/**
 *
 * @author vinipachecov
 */
public class Calculadora {
    
    
    /**
     * 
     * @param v1 corresponde ao primeiro operando de uma soma
     * @param v2 corresonde ao segundo operador de uma soma
     * @return retorna o resultado do calculo da soma
     */
    public float soma(float v1, float v2){
        return v1 + v2;
    }
    /**
     * 
     * @param v1 corresponde ao primeiro operando de uma subtração
     * @param v2 corresponde ao segundo operando de uma subtração
     * @return 
     */
    public float subtrair(float v1, float v2){
        return v1 - v2;
    }    
}
