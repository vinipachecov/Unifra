    private void connectjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectjButtonActionPerformed
        if(!nickTextField.getText().toString().equals("")){
            multicast = new Multicast(adressipTextField.getText().toString(), chatArea, nickTextField.getText().toString(), listModel);
            updateClientList(multicast.getCLientList());
            
        }else
            JOptionPane.showConfirmDialog(this, "DIgite um Nick!", "NIck Inv√°lido", WIDTH);
    }//GEN-LAST:event_connectjButtonActionPerformed

    private void mensagemTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mensagemTextFieldKeyPressed
        String mensagem;
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(mensagemTextField.getText().toString() != ""){
                mensagem = mensagemTextField.getText().toString();
                multicast.sendMessage(mensagem);
                mensagemTextField.setText("");
            }            
        }        
    }//GEN-LAST:event_mensagemTextFieldKeyPressed

